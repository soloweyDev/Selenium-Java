
public class StaticAndNonstatic {
	
	public static int q = 200; //глобальная статическая переменная
	
	public int p = 201; //глобальная нестатическая переменная
	
	// статическая функция
	public static void first_function()
	{
		int abc = 20; //локальное значение
	}
	
	// нестатическая функция
	public void second__function()
	{
		int xyz = 100; //локальная переменая
	}

	public static void main(String[] args) {
		
		//

	}

}
